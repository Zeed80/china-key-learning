def preferred_form(number: int, character: str) -> str:
    for form, variant_type, position, _ in VARIANTS.get(number, []):
        if variant_type in {"simplified", "positional"} or position != "any":
            return form
    return character


def sentence_for_example(example_character: str, example_pinyin: str, radical_form: str) -> tuple[str, str, str]:
    sentence_zh = f"“{example_character}”字里有“{radical_form}”这个部首。"
    readable = example_pinyin or example_character
    sentence_pinyin = f"{readable} zi4 li3 you3 {radical_form} zhe4 ge bu4shou3."
    sentence_ru = f"В иероглифе «{example_character}» есть ключ «{radical_form}»."
    return sentence_zh, sentence_pinyin, sentence_ru


def enrich_example(
    number: int,
    radical_character: str,
    raw: tuple[str, str, str, str] | tuple[str, str, str, str, str, str, str],
) -> tuple[str, str, str, str, str, str, str]:
    if len(raw) == 7:
        return raw
    character, pinyin, translation_ru, note_ru = raw
    sentence_zh, sentence_pinyin, sentence_ru = sentence_for_example(character, pinyin, preferred_form(number, radical_character))
    return character, pinyin, translation_ru, note_ru, sentence_zh, sentence_pinyin, sentence_ru


def default_examples(number: int, radical_character: str, meaning_ru: str) -> list[tuple[str, str, str, str, str, str, str]]:
    form = preferred_form(number, radical_character)
    example_character, pinyin, translation_ru = SIMPLE_EXAMPLES.get(number, (radical_character, "", meaning_ru))
    position = variant_summary(number)
    note = (
        f"Разбор: в «{example_character}» ищите ключ «{form}» как смысловой или графический компонент. "
        f"Он связан с темой «{meaning_ru}». {position} "
        "Для первого прохода цель — уверенно распознать форму и смысловую зону без перегруза этимологией."
    )
    sentence_zh, sentence_pinyin, sentence_ru = sentence_for_example(example_character, pinyin, form)
    examples = [(example_character, pinyin, translation_ru, note, sentence_zh, sentence_pinyin, sentence_ru)]
    if example_character != radical_character:
        base_note = (
            f"Разбор: «{radical_character}» — базовая форма ключа. "
            "Ее полезно узнавать отдельно перед тем, как искать сжатые или упрощенные варианты внутри сложных иероглифов."
        )
        base_sentence_zh, base_sentence_pinyin, base_sentence_ru = sentence_for_example(radical_character, "", radical_character)
        examples.append((radical_character, "", meaning_ru, base_note, base_sentence_zh, base_sentence_pinyin, base_sentence_ru))
    return examples


def examples_for(number: int, radical_character: str, meaning_ru: str) -> list[tuple[str, str, str, str, str, str, str]]:
    curated = EXAMPLES.get(number)
    if curated:
        return [enrich_example(number, radical_character, item) for item in curated]
    return default_examples(number, radical_character, meaning_ru)


def auto_confusable(number: int, strokes: int) -> tuple[str, str]:
    current = next((item for item in RADICALS if item[0] == number), None)
    same_strokes = [item for item in RADICALS if item[0] != number and item[2] == strokes]
    candidates = same_strokes or [item for item in RADICALS if item[0] != number]
    other = min(candidates, key=lambda item: abs(item[0] - number))
    current_char = current[1] if current else "?"
    current_meaning = current[3] if current else "этот ключ"
    other_char = other[1]
    other_meaning = other[3]
    note = (
        f"Графическая пара для тренировки: оба ключа имеют {strokes} {stroke_word(strokes)}. "
        f"У «{current_char}» держите в фокусе [[{current_meaning}]], "
        f"у «{other_char}» — [[{other_meaning}]]. Сравнивайте контур, направление штрихов и позицию внутри знака."
    )
    return other_char, note


def stroke_word(strokes: int) -> str:
    if strokes % 10 == 1 and strokes % 100 != 11:
        return "черту"
    if strokes % 10 in {2, 3, 4} and strokes % 100 not in {12, 13, 14}:
        return "черты"
    return "черт"


def confusables_for(number: int, character: str, strokes: int) -> list[tuple[str, str]]:
    curated = CONFUSABLES.get(number)
    if curated:
        return curated
    return [auto_confusable(number, strokes)]


def semantic_hint(meaning_ru: str) -> str:
    meaning = meaning_ru.lower()
    for keywords, hint in SEMANTIC_HINTS:
        if any(keyword in meaning for keyword in keywords):
            return hint
    return "предмет, действие, качество или форму"


def variant_summary(number: int) -> str:
    variants = VARIANTS.get(number, [])
    if not variants:
        return "Отдельной упрощенной формы здесь нет: в составе знака обычно важно узнавать основной силуэт."

    parts = []
    for form, variant_type, position, note_ru in variants[:3]:
        type_label = "упрощенная" if variant_type == "simplified" else "позиционная" if variant_type == "positional" else "вариантная"
        parts.append(f"{form} — {type_label}, {POSITION_NAMES.get(position, position)}")
    suffix = "; ".join(parts)
    return f"Ключ может менять вид: {suffix}."


def confusable_summary(number: int) -> str:
    confusables = CONFUSABLES.get(number, [])
    if not confusables:
        return "Главная проверка при чтении — число черт, пропорции и место компонента внутри иероглифа."
    other_form, note_ru = confusables[0]
    return f"Не путайте с «{other_form}»: {note_ru}"


def default_description(number: int, character: str, meaning_ru: str) -> str:
    return (
        f"«{character}» — ключ №{number}: {meaning_ru}. "
        f"В иероглифах он чаще всего указывает на сферу: {semantic_hint(meaning_ru)}; это не перевод всего знака, а смысловой ориентир. "
        f"{variant_summary(number)} {confusable_summary(number)}"
    )


def default_usage(number: int, strokes: int) -> str:
    variants = VARIANTS.get(number, [])
    positions = [POSITION_NAMES.get(position, position) for _, _, position, _ in variants if position != "any"]
    position_text = ", ".join(dict.fromkeys(positions)) if positions else "в основной форме или как компактный компонент"
    return (
        f"Группа по числу черт: {strokes}. В составе иероглифа ищите ключ {position_text}. "
        "Сначала распознайте контур и позицию, затем проверяйте смысл: один и тот же ключ может давать только общий семантический намек."
    )


def default_mnemonic(number: int, character: str, meaning_ru: str) -> str:
    core = meaning_ru.split(";")[0].strip()
    form = preferred_form(number, character)
    variant_text = f" Если в составе знак сжимается, сначала ищите форму «{form}»." if form != character else ""
    confusable = CONFUSABLES.get(number, [])
    compare_text = f" Контрольное отличие: {confusable[0][1]}" if confusable else " Контрольное отличие: сравните силуэт с ближайшим ключом того же числа черт."
    return (
        f"Фокус запоминания: «{character}» = {core}. "
        "Сначала узнавайте контур, потом привязывайте его к смысловой зоне, а не к длинному переводу."
        f"{variant_text}{compare_text}"
    )
